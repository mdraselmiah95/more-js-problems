// Two numbers add
function add(num1, num2) {
    const total = num1 + num2;
    return total;
}

var totalSum = add(23, 27);
console.log(totalSum);


// Two numbers multiply
function multiply(num1, num2) {
    const total = num1 * num2;
    return total;
}

var totalMultiply = multiply(3, 33);
console.log(totalMultiply);